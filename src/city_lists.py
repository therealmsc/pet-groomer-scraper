def get_cities_for_state(state_name: str) -> list[str]:
    """Return the list of cities for a given state name."""
    state_key = state_name.lower().strip()
    if state_key not in STATE_CITIES:
        raise ValueError(f"Unknown state: {state_name}. Available: {sorted(STATE_CITIES.keys())}")
    return list(STATE_CITIES[state_key])
